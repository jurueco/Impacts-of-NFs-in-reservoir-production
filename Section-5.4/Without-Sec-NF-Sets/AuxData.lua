function PostRest(countP,t, wfile1)
  -- Get the needed accessors.
  local mesh = modelData:mesh('mesh')
  if(countP == 0) then
     wfile1:write( 'Gid Post Results File 1.0', '\n', '\n')
     wfile1:write( 'GaussPoints "Solids" Elemtype Quadrilateral', '\n')
     wfile1:write( 'Number of Gauss Points:  4  ', '\n')
     wfile1:write( 'Natural coordinates: Internal', '\n')
     wfile1:write( 'END gausspoints', '\n', '\n')
     wfile1:write( 'GaussPoints "Interface" Elemtype Linear', '\n')
     wfile1:write( 'Number of Gauss Points:  2  ', '\n')
     wfile1:write( 'Natural coordinates: Internal', '\n')
     wfile1:write( 'END gausspoints', '\n', '\n')          
  end
  
  local accAf = mesh:gaussAttributeAccessor('An',1, true)
  wfile1:write(string.format('%s%12.4e%s\n','ResultGroup "Printed Variables" ',t,' OnGaussPoints "Interface" '))
  wfile1:write( '    ResultDescription "Open_(m)" Scalar', '\n')          
  wfile1:write( '    Values', '\n')    
  for i=1, mesh:numCells() do  
      local e = mesh:cell(i)  -- element
      local ir  = mesh:elementIntegrationRule(e:type(), 1)
      -- Interface element
      if(e:type() == 'int2dl4' or e:type() == 'int3dl6' or e:type() == 'int3dl8') then
         -- for each integration poin
         for j=1, ir:numPoints() do
             local pi = j         
             open  = accAf:value(e, pi)			 
             if(j == 1) then         
                wfile1:write(string.format('%9d%s%12.4e\n', e:id(),' ',open))   
             else                         
                wfile1:write(string.format('%s%21.4e\n',' ', open))   
             end
        end
      end          
  end  
  wfile1:write( '    End Values', '\n\n')
  
  wfile1:write(string.format('%s%15.6e%s\n','ResultGroup "Printed Variables" ',t,' OnNodes'))
  wfile1:write( '    ResultDescription "Poro_M(kPa)" Scalar', '\n') 
  wfile1:write( '    Values', '\n')
  local accPore  = assert(mesh:nodeValueAccessor('P'))
  for ii=1, mesh:numNodes() do 
      local pore = accPore:value(ii) 
      wfile1:write(string.format('%9d%14.4e \n', ii,  pore))         
  end 
  wfile1:write( '    End Values', '\n\n')
end

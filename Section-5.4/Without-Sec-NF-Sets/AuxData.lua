function PostMesh (wfile1)
    local mesh = modelData:mesh('mesh')
    -- Node coordinate accessors
    local coordAc = mesh:nodeCoordAccessor()  
    wfile1:write( 'MESH dimension 2 ElemType Quadrilateral  Nnode    4', '\n')
    wfile1:write( 'Coordinates', '\n')

    for ii=1, mesh:numNodes() do
        local xy = coordAc:value(ii) 
        wfile1:write(string.format('%9d%15.7e%15.7e \n', ii,  xy(1), xy(2)))
    end  
    wfile1:write( 'End coordinates', '\n\n')
        
    -- solid element
    wfile1:write( 'Elements', '\n')
    for i=1, mesh:numCells() do  
        local e = mesh:cell(i)  -- element
        local idmat = e:propertyIndex(1)
        if(e:type() == 'quad4') then
           wfile1:write(string.format('%9d%9d%9d%9d%9d%9d\n', e:id(), e:nodeIndex(1) ,e:nodeIndex(2),e:nodeIndex(3),e:nodeIndex(4), idmat  ))
       end          
    end  
    wfile1:write( 'END elements', '\n\n')  
    --Interface
    -- Get the needed accessors.
    local mesh = modelData:mesh('mesh')
    wfile1:write( 'MESH dimension 2 ElemType Linear  Nnode    2', '\n')
    wfile1:write( 'Coordinates', '\n')        
    wfile1:write( 'End coordinates', '\n\n')
    wfile1:write( 'Elements', '\n')
    for i=1, mesh:numCells() do  
        local e = mesh:cell(i)  -- element
        local idmat = e:propertyIndex(1)
        if(e:type() == 'int2dl4') then
           wfile1:write(string.format('%9d%9d%9d%9d\n', e:id(), e:nodeIndex(1) ,e:nodeIndex(2), idmat  ))
        end          
    end  
    wfile1:write( 'END elements', '\n')  
end

function UpdateOpening()
   local mesh = modelData:mesh('mesh')
   local accAf  = mesh:gaussAttributeAccessor('An',1,true)
   local aperture = dofile('$SIMULATIONDIR/aperture.lua')
   for i=1, #aperture do
       local e = mesh:cell(aperture[i][1])
	   local ir  = mesh:elementIntegrationRule(e:type(), 1)
       local j = aperture[i][2]
	   local pfopen = aperture[i][3]
       accAf:setValue(e, j, pfopen)  
	   --print(string.format(' EL pt A0 %9d %2d %15.5e',aperture[i][1],ir, pfopen ))
   end

end

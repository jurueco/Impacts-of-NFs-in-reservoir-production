-------------------------------------------------------------
 function volumeReactFlux (countP, t, wfile1, dt, vFTm)
    -- Get the needed accessors.
    local mesh = modelData:mesh('mesh')
    -- node coordinate accessors
    local coordAc = mesh:nodeCoordAccessor()
        -- node reaction volume flux accessor
    local rvfAc  = assert(mesh:nodeValueAccessor('vFm', 'm^3/s'))           -- concentration


    -- List nodes inFlow 29 and out flow 29
    local inQ  = {    }
    -- List nodes outFlow
    local outQ = { 3081}

    local InF = 0.0   -- total in flow matrix and fracture
    local outF = 0.0  -- total out flow matrix and fracture
    local InFm = 0.0
    local InFfr = 0.0
    local OutFm = 0.0
    local OutFfr = 0.0        

    for j = 1, #inQ do
        local Rf = rvfAc:value(inQ[j])
        InFm = InFm + Rf(1,1)
        InF  = InF  + Rf(1,1)  
    end        
        
    for j = 1, #outQ do
        local Rf = rvfAc:value(outQ[j])
        OutFm = OutFm + Rf(1,1)
        outF  = outF  + Rf(1,1) 
    end        
        
        -- print results
    if (countP == 1) then
        wfile1:write( 'Increment','    Time    ','   Vol_In_M ','  Vol_In_Fr ','   Vol_Out_M','  Vol_Out_Fr','    Q_in    ','    Q_Out   ','   Vol_In   ','   Vol_Out', '\n')
    end
    -- total injecter volume flux matriz and fracture
    vFTm[1] = vFTm[1] + InFm*dt   
    vFTm[2] = vFTm[2] + InFfr*dt 
    -- total producer volume flux matriz and fracture
    vFTm[3] = vFTm[3] - OutFm*dt   
    vFTm[4] = vFTm[4] - OutFfr*dt         
    -- cumulative production matriz + fracture
    vFTm[5] = vFTm[5] + InF*dt                
    vFTm[6] = vFTm[6] - outF*dt        
    wfile1:write(string.format('%9d%12.2e%12.2e%12.2e%12.2e%12.2e%12.2e%12.2e%12.2e%12.2e \n',countP,t,vFTm[1],vFTm[2],vFTm[3],vFTm[4],InF,-outF,vFTm[5],vFTm[6]))        
    return vFTm
 end

